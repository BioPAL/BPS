def get_rx_gain_from_parameters_code(gain_parameter_code: int):
    """Get the RX gain from the gain parameter code."""
    if not (0 <= gain_parameter_code < len(GAIN_TABLE)):
        raise ValueError(f"Code out of range. Must be between 0 and {len(GAIN_TABLE) - 1}.")
    return GAIN_TABLE[gain_parameter_code]
